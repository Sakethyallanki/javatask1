package variables;

public class Demo1 {
	
	//public -- Access Modifier(Private, public ,protected and default)
	//static -- Access Specifier (static and Non-static)
	//void -- Return type (void, data type and Object)
	//main -- Method Name (As your wish)
	//() -- Parameters(Data types and Object)
	public static void main(String[] args) {
		//Primitive Data Types
		int i=100;
		short s=10;
		long l=1000l;
		byte b=1;
		float f=10.36f;
		double d=1000.96;
		boolean bl=true;
		char ch='A'; //A-Z or a-z or 0-9 or !@#$%^&*()
		
		//Non Primitive Data Types
		String string="Hello"; //Collection of characters.
		
		System.out.print(i);
		System.out.print(s);
		System.out.print(l);
		System.out.print(b);
		System.out.println(f);
		System.out.println(d);
		System.out.println(bl);
		System.out.println(ch);
		System.out.println(string);
	}
	
}
